// El IF, ELSE y ELSE IF.
let clima = 'soleado';
let dia = 'domingo';

if (clima == 'soleado' && dia == 'domingo'){
    console.log('Este es el IF');
}

if (clima == 'soleado' && dia == 'domingo'){
    console.log('Este es el IF');
}else {
    console.log('Este es el ELSE')
}

if (clima == 'soleado' && dia == 'domingo'){
    console.log('Este es el IF');
}else if (clima == 'soleado' && dia != 'domingo'){
    console.log('Este es el ELSE IF')
}else {
    console.log('Este es el ELSE')
}